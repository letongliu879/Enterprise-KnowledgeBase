# Main generation logic
# ---------------------------------------------------------------------------


def load_models():
    """Dynamically import SQLAlchemy models from the persistence package.

    Returns a dict mapping table_name → SQLAlchemy model class.
    """
    # Add the project root to sys.path
    project_root = Path(__file__).resolve().parent.parent
    models_path = project_root / "packages" / "persistence" / "src"
    sys.path.insert(0, str(models_path))

    from reality_rag_persistence.models import Base

    # Collect all model classes registered on Base
    models = {}
    for mapper in Base.registry.mappers:
        cls = mapper.class_
        table_name = cls.__tablename__
        models[table_name] = cls
    return models


def generate_table_ddl(table_name: str, model_cls) -> str:
    """Generate CREATE TABLE DDL from a SQLAlchemy model class."""
    cols = []
    pk_constraints = []
    for col in model_cls.__table__.columns:
        col_ddl = column_ddl(col)

        # Track composite primary keys separately
        if col.primary_key and not col.autoincrement:
            # Single PK is inlined — skip composite handling
            pass

        cols.append(col_ddl)

    # Handle composite primary keys
    pk_cols = [c for c in model_cls.__table__.columns if c.primary_key]
    if len(pk_cols) > 1:
        # Exclude autoincrement from composite PK
        pk_names = [c.name for c in pk_cols]
        pk_parts = ", ".join(pk_names)
        pk_constraints.append(f"    PRIMARY KEY ({pk_parts})")

    col_lines = ",\n".join(cols + pk_constraints)
    return f"CREATE TABLE IF NOT EXISTS {table_name} (\n{col_lines}\n);\n"


def generate_module_schema(module: str, models: dict) -> str:
    """Generate the full schema.sql content for one module."""
    lines = [
        f"-- Auto-generated schema for Java {module} service tests.\n",
        f"-- Source of truth: SQLAlchemy models in packages/persistence/src/reality_rag_persistence/models.py\n",
        f"-- and hardcoded DDL for Java-only projection tables.\n",
        f"-- Regenerate with: uv run python scripts/generate_java_test_schema.py\n",
    ]

    for table_name in MODULE_TABLES[module]:
        lines.append(f"\n-- {table_name}")
        if table_name in JAVA_ONLY_TABLES:
            lines.append(JAVA_ONLY_TABLES[table_name])
        elif table_name in models:
            lines.append(generate_table_ddl(table_name, models[table_name]))
        else:
            print(f"WARNING: table '{table_name}' not found in models or hardcoded tables — skipping", file=sys.stderr)

    return "\n".join(lines)


def read_current_schema(path: Path) -> Optional[str]:
    """Read existing schema.sql, or None."""
    if path.exists():
        return path.read_text(encoding="utf-8")
    return None


def main():
    parser = argparse.ArgumentParser(description="Generate Java test schema.sql from SQLAlchemy models")
    parser.add_argument("--check", action="store_true", help="Check-only mode: diff against existing, exit 1 on mismatch")
    parser.add_argument("--output-dir", choices=["access", "retrieval"], help="Generate for a single module only")
    args = parser.parse_args()

    project_root = Path(__file__).resolve().parent.parent
    models = load_models()

    modules = [args.output_dir] if args.output_dir else ["access", "retrieval"]
    any_mismatch = False

    for module in modules:
        generated = generate_module_schema(module, models)
        schema_path = project_root / "services" / module / "src" / "test" / "resources" / "schema.sql"

        if args.check:
            existing = read_current_schema(schema_path)
            if existing is None:
                print(f"[MISMATCH] {schema_path}: file does not exist", file=sys.stderr)
                any_mismatch = True
                continue

            if existing.strip() != generated.strip():
                print(f"[MISMATCH] {schema_path}: content differs from generated DDL", file=sys.stderr)
                # Show diff
                import difflib
                diff = difflib.unified_diff(
                    existing.splitlines(),
                    generated.splitlines(),
                    fromfile=str(schema_path),
                    tofile="generated",
                    lineterm="",
                )
                for line in diff:
                    print(line, file=sys.stderr)
                any_mismatch = True
            else:
                print(f"[OK] {schema_path}")
        else:
            schema_path.parent.mkdir(parents=True, exist_ok=True)
            schema_path.write_text(generated, encoding="utf-8")
            print(f"[WRITE] {schema_path}")

    if args.check and any_mismatch:
        sys.exit(1)


if __name__ == "__main__":
    main()
