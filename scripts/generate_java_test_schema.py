#!/usr/bin/env python3
"""
Generate PostgreSQL DDL for Java test schema.sql from SQLAlchemy models.

Two sources of truth:
  1. SQLAlchemy models (for tables used by both Python and Java)
  2. Hardcoded DDL (for Java-only projection tables not mirrored in models.py)

Usage:
  uv run python scripts/generate_java_test_schema.py                # write to both modules
  uv run python scripts/generate_java_test_schema.py --check         # diff-only, exit 1 on mismatch
  uv run python scripts/generate_java_test_schema.py --output-dir access  # single module
"""

import argparse
import re
import sys
from pathlib import Path
from typing import Optional

# ---------------------------------------------------------------------------
# SQLAlchemy type → PostgreSQL DDL type mapping
# ---------------------------------------------------------------------------

PG_TYPE_MAP = {
    "String": "VARCHAR({length})",
    "Integer": "INTEGER",
    "Float": "DOUBLE PRECISION",
    "Boolean": "BOOLEAN",
    "DateTime": "TIMESTAMP",
    "JSON": "TEXT",
    "LargeBinary": "BYTEA",
}


def sa_type_to_pg(col) -> str:
    """Convert a SQLAlchemy Column's type to a PostgreSQL DDL type string."""
    col_type = col.type
    type_name = type(col_type).__name__

    if type_name == "String":
        length = col_type.length or 255
        return f"VARCHAR({length})"
    elif type_name == "Integer":
        return "INTEGER"
    elif type_name == "Float":
        return "DOUBLE PRECISION"
    elif type_name == "Boolean":
        return "BOOLEAN"
    elif type_name in ("DateTime", "TIMESTAMP"):
        return "TIMESTAMP"
    elif type_name == "JSON":
        return "TEXT"
    elif type_name == "LargeBinary":
        return "BYTEA"
    else:
        raise ValueError(f"Unknown SA type {type_name} for column {col.name}")


def column_ddl(col, indent: str = "    ") -> str:
    """Generate a single column DDL line from a SQLAlchemy Column."""
    col_type_name = type(col.type).__name__
    parts = [indent, col.name, " ", sa_type_to_pg(col)]

    if not col.nullable:
        parts.append(" NOT NULL")

    # Autoincrement identity for Integer PKs
    is_auto_pk = col.autoincrement and col.primary_key and col_type_name == "Integer"

    if is_auto_pk:
        parts.append(" GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY")
        return "".join(parts)

    # Default values
    if col.default is not None and hasattr(col.default, "arg"):
        default = col.default.arg
        if isinstance(default, bool):
            parts.append(f" DEFAULT {'true' if default else 'false'}")
        elif isinstance(default, int):
            parts.append(f" DEFAULT {default}")
        elif isinstance(default, str):
            if default in ("", None):
                pass
            else:
                parts.append(f" DEFAULT '{default}'")
        elif isinstance(default, float):
            parts.append(f" DEFAULT {default}")
        elif callable(default):
            pass

    # Only inline PRIMARY KEY for single-column PKs, not composite
    if col.primary_key:
        # Check how many PK columns this table has
        pk_count = sum(1 for c in col.table.columns if c.primary_key)
        if pk_count == 1:
            parts.append(" PRIMARY KEY")

    return "".join(parts)


